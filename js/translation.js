function getNestedValue(obj, path) {
    return path.split('.').reduce((current, key) => current && current[key], obj);
}

function translate(lang = 'ru') {
    const langData = translations[lang] || translations.ru;
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getNestedValue(langData, key);
        
        if (translation) {
            element.textContent = translation;
        }
    });
    
    const h1 = document.querySelector('.my_nike');
    if (h1) {
        const tooltipAttr = `data-tooltip-${lang}`;
        const tooltip = h1.getAttribute(tooltipAttr);
        if (tooltip) {
            h1.setAttribute('data-tooltip', tooltip);
        }
    }
    
    localStorage.setItem('selectedLanguage', lang);
    document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('selectedLanguage') || 'ru';
    translate(savedLang);
    
    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', function(e) {
            e.preventDefault();
            const lang = this.getAttribute('data-lang');
            translate(lang);
        });
    });
});

